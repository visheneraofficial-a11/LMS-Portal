// ─── Attendance Data ───
export function generateAttendance(month = 2, year = 2026) {
  const daysInMonth = new Date(year, month, 0).getDate();
  const holidays = [];
  const data = [];
  for (let d = 1; d <= daysInMonth; d++) {
    const date = new Date(year, month - 1, d);
    const dow = date.getDay();
    let status;
    if (dow === 0) { status = 'holiday'; holidays.push(d); }
    else if (d > 17) { status = 'future'; }
    else {
      const seed = d * 17 + 15;
      status = (seed % 100 > 12) ? 'present' : 'absent';
    }
    data.push({ day: d, date: date.toISOString().split('T')[0], status, dayName: ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'][dow] });
  }
  return data;
}

